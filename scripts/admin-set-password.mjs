#!/usr/bin/env node
/**
 * admin-set-password.mjs
 *
 * Genera o SQL UPSERT para definir/forçar a senha do admin diretamente
 * em Supabase, sem passar pelo flow web. Útil em três casos:
 *   - bootstrap inicial sem usar a env var ADMIN_BLOG_PASSWORD;
 *   - recuperação manual quando o reset por e-mail não está disponível;
 *   - rotação de senha de emergência (também invalida toda sessão ativa,
 *     pois bumpa sessions_invalidated_at = now()).
 *
 * Uso:
 *   node scripts/admin-set-password.mjs
 *
 * O script:
 *   1. pede a nova senha em modo silencioso (não fica no histórico);
 *   2. valida tamanho mínimo (≥ 8 caracteres);
 *   3. gera o hash scrypt no mesmo formato que src/lib/admin/users.ts;
 *   4. monta o SQL UPSERT contra admin_users;
 *   5. copia o SQL para o clipboard (macOS: pbcopy).
 *
 * Você pega o SQL no clipboard e cola no SQL Editor do Supabase,
 * dashboard → SQL Editor → New query → Cmd+V → Run.
 */

import { randomBytes, scryptSync } from 'node:crypto';
import { spawnSync } from 'node:child_process';
import readline from 'node:readline';
import { Writable } from 'node:stream';

const ADMIN_EMAIL = process.env.ADMIN_EMAIL || 'contato@dm2contabilidade.com.br';

function readSilently(promptText) {
  return new Promise((resolve) => {
    // A muted writable so the typed characters never echo to the terminal.
    const muted = new Writable({
      write(chunk, _enc, cb) {
        // Allow the prompt itself to be visible (printed before this stream
        // takes over). Subsequent writes from readline (echo of keystrokes)
        // are silently dropped.
        cb();
      },
    });
    const rl = readline.createInterface({
      input: process.stdin,
      output: muted,
      terminal: true,
    });
    process.stdout.write(promptText);
    rl.question('', (answer) => {
      rl.close();
      process.stdout.write('\n');
      resolve(answer);
    });
  });
}

function hashPassword(plain) {
  const salt = randomBytes(16);
  const hash = scryptSync(plain, salt, 64, { N: 16384, r: 8, p: 1 });
  return `scrypt$N=16384,r=8,p=1$${salt.toString('base64')}$${hash.toString('base64')}`;
}

function sqlEscape(value) {
  return value.replace(/'/g, "''");
}

function buildSql({ email, hash }) {
  return `-- Generated by scripts/admin-set-password.mjs
-- Run in Supabase SQL Editor.
INSERT INTO admin_users (email, password_hash)
VALUES ('${sqlEscape(email)}', '${sqlEscape(hash)}')
ON CONFLICT (email) DO UPDATE
  SET password_hash = EXCLUDED.password_hash,
      password_updated_at = now(),
      sessions_invalidated_at = now();
`;
}

function copyToClipboard(text) {
  const result = spawnSync('pbcopy', [], { input: text });
  return result.status === 0;
}

async function main() {
  const pwd1 = await readSilently(`Nova senha para ${ADMIN_EMAIL} (mín. 8 chars, não será exibida): `);
  if (!pwd1 || pwd1.length < 8) {
    console.error('ERRO: a senha deve ter ao menos 8 caracteres.');
    process.exit(1);
  }
  const pwd2 = await readSilently('Confirme a nova senha: ');
  if (pwd1 !== pwd2) {
    console.error('ERRO: as senhas não coincidem.');
    process.exit(1);
  }

  const hash = hashPassword(pwd1);
  const sql = buildSql({ email: ADMIN_EMAIL, hash });

  const copied = copyToClipboard(sql);

  console.log('');
  console.log('Hash gerado com sucesso.');
  console.log('');
  if (copied) {
    console.log('SQL copiado para o clipboard. Cole no SQL Editor do Supabase e clique Run.');
  } else {
    console.log('Não foi possível copiar para o clipboard. Cole manualmente o SQL abaixo:');
    console.log('');
    console.log(sql);
  }
  console.log('');
  console.log('Após rodar o SQL, todas as sessões anteriores ficam inválidas.');
  console.log('Faça login em /admin/blog/login com a nova senha.');
}

main().catch((err) => {
  console.error('ERRO:', err.message);
  process.exit(1);
});
